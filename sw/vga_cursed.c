void vga_draw_image(void) {
    int x = 0;
    int y = 0;
    vga_set_cursor_pos(x, y);

    for(int i = 0; i < VGA_IMAGE_RUNS; i++) {
        vga_rle_cell_t run = vga_image_data[i];
        vga_color_t fg = (vga_color_t)(run.attribute & 0x0F);
        vga_color_t bg = (vga_color_t)((run.attribute >> 4) & 0x0F);

        for(int k = 0; k < run.count; k++) {
            vga_putc(run.character, fg, bg);
            x++;
            if (x >= 80) {
                x = 0;
                y++;
                vga_set_cursor_pos(x, y);
            }
        }
    }
}

int main() {
    vga_init();
    vga_clear(VGA_COLOR_WHITE, VGA_COLOR_BLUE);

    set_interrupt_enable(1, IRQ_VGA);
    set_global_irq_enable(1);
    vga_set_interrupt_enable(true);
    
    wfi();

    vga_draw_image();
    
    wfi();
    wfi();
    
    return 0;
}
